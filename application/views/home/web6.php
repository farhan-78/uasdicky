        <center><h2>THE ETERNAL HUNTERS</h2></center>

          <p>Separate, but never parted, Kindred represents the twin essences of death. Lamb’s bow offers a swift release from the mortal realm for those who accept their fate. Wolf hunts down those who run from their end, delivering violent finality within his crushing jaws. 
              Though interpretations of Kindred’s nature vary across Runeterra, every mortal must choose the true face of their death.</p>

          <p>Kindred is the white embrace of nothingness and the gnashing of teeth in the dark. Shepherd and the butcher, poet and the primitive, they are one and both. When caught on the edge of life, louder than any trumpeting horn, it is the hammering pulse at one’s throat that calls Kindred to their hunt. 
            Stand and greet Lamb’s silvered bow and her arrows will lay you down swiftly. If you refuse her, Wolf will join you for his merry hunt, where every chase runs to its brutal end.</p>
			
          <p>For as long as its people have known death, Kindred has stalked Valoran. When the final moment comes, it is said a true Demacian will turn to Lamb, taking the arrow, while through the shadowed streets of Noxus, Wolf leads the hunt. In the snows of the Freljord, before going off to fight, some warbands “kiss the Wolf,” 
            vowing to honor his chase with the blood of their enemies. After each Harrowing, the town of Bilgewater gathers to celebrate its survivors and honor those granted a true death by Lamb and Wolf.</p>
		  
		  <p>Denying Kindred is to deny the natural order of things. There are but a wretched few who have eluded these hunters. This perverse escape is no sanctuary, for it only holds a waking nightmare. Kindred waits for those locked in the undeath of the Shadow Isles, for they know all will eventually fall to Lamb’s bow or Wolf’s teeth.</p> 
		  
		  <p>The earliest dated appearance of the eternal hunters is from a pair of ancient masks, carved by unknown hands into the gravesites of people long-forgotten. But to this day, Lamb and Wolf remain together, and they are always Kindred.</p>
            
